package net.bati.guilib.utils;

public class Mouse {
    private int x;
    private int y;
    private boolean dragging;

    public Mouse() {
    }

    public void setX(int v) {
        this.x = v;
    }

    public int getX() {
        return x;
    }

    public void setY(int v) {
        this.y = v;
    }

    public int getY() {
        return y;
    }
}

